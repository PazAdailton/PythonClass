friends = ["Rolf", "Charlie", "Anna", "Bob", "jen"]

#o primeiro número define o começo da lista
print(friends[1:2])
# o segundo define o final
print(friends[2:4])

#imprime todos elementos da lista exceto o primeiro
print(friends[1:])

#imprime a partir dos 3 primeiros elementos
print(friends[-3:])
#a partir do 3 e até o 4
print(friends[-3:4])

#exceto os dois últimos
print(friends[:-2])